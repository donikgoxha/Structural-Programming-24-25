/*
Со помош на знаците ‘@’,'%', и ‘.‘ се формира квадратна слика со „должина“ m (m>2)
која претставува квадрат од знаците '.', впишан во квадрат од знаците @ и %(види примери). З
а внесено m да се испечати на екран соодветната слика. Внимавајте – не печатете нити еден
дополнителен знак, како празно место, непотребен знак за нов ред... Сликата завршува со знак за нов ред.
Забелешка: Бидејќи е дадено ограничување за m, во точно решение на задачата не треба да се користи
низа/матрица.
 */
#include <iostream>
using namespace std;

int main() {
    int n;
    cin >> n;

    if (n <= 2) {
        cout << "Invalid input!" << endl;
    }
    cout << "%";
    for (int i = 1; i < n - 1; i++) {
        cout << "@";
    }
    cout << "%" << endl;

    for (int i = 0; i < n - 2; i++) {
        cout << "%";
        for (int j = 0; j < n - 2; j++) {
            cout << ".";
        }
        cout << "%" << endl;
    }

    cout << "%";
    for (int i = 1; i < n - 1; i++) {
        cout << "@";
    }
    cout << "%" << endl;
    return 0;
}
